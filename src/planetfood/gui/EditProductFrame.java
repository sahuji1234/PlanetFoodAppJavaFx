        public void loadProductDetails(){
        try{
            categories=CategoryDao.getAllCategoryId();
            Collection s=categories.keySet();
            Iterator it=s.iterator();
            while(it.hasNext()){
                jcCatName.addItem((String)it.next());
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in Categories!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        }
        
        public void clearAll(){
            jcProdId.removeAllItems();
            txtProdName.setText("");
            txtProdPrice.setText("");
        }
        
        public int validateInput(){
            int result =1;
            prodName=txtProdName.getText();
            if(rbIsActive.isSelected())
                prodIsActive="Y";
            else
                prodIsActive="N";
            
            try{
                prodPrice=Double.parseDouble(txtProdPrice.getText());
            }
            catch(Exception ex){
                result=0;
                ex.printStackTrace();
            }
            if(prodName.isEmpty()){
                result=-1;
            }
            return result;
        }
    
