    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int validate =validateInput();
        if(validate==0)
        {
            JOptionPane.showMessageDialog(null,"Input Product Name!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(validate==-1){
            JOptionPane.showMessageDialog(null,"Input Product Price!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try{
            p.setCatId(catId);
            p.setProdPrice(prodPrice);
            p.setProdId(prodId);
            p.setIsActive(prodIsActive);
            p.setProdName(prodName);
            if(ProductDao.UpdateProduct(p)){
                JOptionPane.showMessageDialog(null,"Product Updated Successfully!","Success",JOptionPane.INFORMATION_MESSAGE);
            }
            else
                JOptionPane.showMessageDialog(null,"Unable to Update!","Error",JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in Products!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void jcCatNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcCatNameActionPerformed
        // TODO add your handling code here:
        catName=jcCatName.getSelectedItem().toString();
        catId=categories.get(catName);
        clearAll();
        try{
            productList=ProductDao.getProductsByCategory(catId);
            
            for(String prodId:productList.keySet())
                jcProdId.addItem(prodId);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB!"+ex,"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcCatNameActionPerformed

    private void jcProdIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcProdIdActionPerformed
        // TODO add your handling code here:
        if(jcProdId.getSelectedItem()==null)
            return;
        prodId=jcProdId.getSelectedItem().toString();
        p=productList.get(prodId);
        txtProdName.setText(p.getProdName());
        txtProdPrice.setText(String.valueOf(p.getProdPrice()));
        prodIsActive=p.getIsActive();
        
        if(prodIsActive.equals("Y"))
            rbIsActive.setSelected(true);
        else
            rbIsActive.setSelected(false);
    }//GEN-LAST:event_jcProdIdActionPerformed

