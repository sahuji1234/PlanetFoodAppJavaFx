    private void jcbEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbEmpIdActionPerformed
        // TODO add your handling code here:
        try{
            ArrayList<Employees> employees=EmployeesDao.getAllEmployees();
            String a=(String)jcbEmpId.getSelectedItem();
            for(Employees e:employees){
                if(a.equals(e.getEmpid())){
                    txtEmpName.setText(e.getEmpname());
                    txtJob.setText(e.getJob());
                    txtSal.setText(Integer.toString(e.getSal()));
                }
            }
                
            
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error in Employees!","Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
    }//GEN-LAST:event_jcbEmpIdActionPerformed

