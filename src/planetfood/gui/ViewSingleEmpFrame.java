        public void loadEmpDetails(){
        try{
            ArrayList<Employees> employees=EmployeesDao.getAllEmployees();
            if(employees.isEmpty()==true){
                    JOptionPane.showMessageDialog(null,"Error in Emp Ids!","Error",JOptionPane.ERROR_MESSAGE);
                }
                else{
                    for(Employees e:employees)
                    {
                        String S=e.getEmpid();
                        jcbEmpId.addItem(S);
                    }
                
            }
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error in Employees!","Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    
