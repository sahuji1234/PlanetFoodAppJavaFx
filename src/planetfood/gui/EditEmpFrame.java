        private boolean validateInput(){
        if(txtEmpName.getText().isEmpty() || txtSal.getText().isEmpty())
            return false;
        else
            return true;
        }
        
        public void showEmpId(){
            try{
                ArrayList<String> IdList=EmployeesDao.getAllEmployeesId();
                if(IdList.isEmpty()==true){
                    JOptionPane.showMessageDialog(null,"Error in Emp Ids!","Error",JOptionPane.ERROR_MESSAGE);
                }
                else{
                    for(String S:IdList)
                    {
                        txtEmpId.addItem(S);
                    }
                
            }
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error in Employees!","Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
        
