        public void loadCashierDetails(){
        try{
            cashier=EmployeesDao.getCashier();
            Collection s=cashier.keySet();
            Iterator it=s.iterator();
            while(it.hasNext()){
                jcEmpName.addItem((String)it.next());
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in Employees!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        }
        
        public boolean validateInput(){
            char[] pwd=pw.getPassword();
            char[] repw=repwd.getPassword();
            if(txtUserId.getText().isEmpty()||pwd.length<4||repw.length<4)
                   return false;
            else
                return true;
        }
    
        public boolean passwordMatch(String a, String b){
            if(a.equals(b))
                return true;
            else
                return false;
        }   
        
