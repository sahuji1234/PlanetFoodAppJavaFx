    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        if(validateInput()==false){
            JOptionPane.showMessageDialog(null,"Please fill all the fields", "Empty Values!!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            cashier=EmployeesDao.getCashier();
            String name=jcEmpName.getSelectedItem().toString();
            String id=cashier.get(name);
            String userId=txtUserId.getText();
            char[] pwd=pw.getPassword();
            String pwsd=String.valueOf(pwd);
            char[] repwd1=repwd.getPassword();
            String repwsd=String.valueOf(repwd1);
            boolean ab=passwordMatch(pwsd,repwsd);
            if(ab==false){
                JOptionPane.showMessageDialog(null,"Password Does Not Match", "Empty Values!!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            User user=new User();
            user.setUserid(userId);
            user.setUserName(name);
            user.setEmpId(id);
            user.setPassword(pwsd);
            user.setUserType("cashier");
            boolean a=UserDao.addCashier(user);
            if(a==true)
                JOptionPane.showMessageDialog(null,"Record Successfully Added To The Database!!", "Success!!", JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(null,"Could Not Add Cashier To The Database!!", "Error!!", JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In Database", "Empty Values!!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

