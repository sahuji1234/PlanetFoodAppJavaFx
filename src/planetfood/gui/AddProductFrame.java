    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
        boolean isValidInput=validateInput();
       
       
       if(isValidInput==false){
           JOptionPane.showMessageDialog(null,"UserId or Password cannot be left empty!","Error",JOptionPane.ERROR_MESSAGE);
           return;
       }
       String isavailable=isAvailable();
       if(isavailable==null){
           JOptionPane.showMessageDialog(null,"Please Choose User type","Error",JOptionPane.ERROR_MESSAGE);
           return;
       }
       try{
       String cat1=(String)jcbCategory.getSelectedItem();
       HashMap cat2=CategoryDao.getAllCategoryId();
       Object key=cat2.get((Object)cat1);
       
       double a=Double.parseDouble(txtPrice.getText());
       Product p=new Product();
       p.setProdId(txtProdCode.getText());
       p.setCatId((String)key);
       p.setProdName(txtName.getText());
       p.setProdPrice(a);
       p.setIsActive(isavailable);
       
       boolean ab=ProductDao.addProduct(p);
       if(ab==false){
           JOptionPane.showMessageDialog(null,"Product Not Added!","Error",JOptionPane.ERROR_MESSAGE);
            
       }
       else{
           JOptionPane.showMessageDialog(null,"Product Added Succesfully","SUCCESS",JOptionPane.INFORMATION_MESSAGE);
           txtProdCode.setText("");
           txtName.setText("");
           txtPrice.setText("");
           loadProductDetails();
       }
       }
       catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"SQL Error!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
       }
      
       
       
       
       
    }//GEN-LAST:event_btnAddProductActionPerformed

