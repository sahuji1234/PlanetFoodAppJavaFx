    private boolean validateInput(){
        if(txtName.getText().isEmpty() || txtPrice.getText().isEmpty())
            return false;
        else
            return true;
    }
    private String isAvailable(){
        if(jrbY.isSelected())
            return "Y";
        else if(jrbN.isSelected())
            return "N";
        else 
            return null;
    }
    
    public void loadProductDetails(){
        try{
            String str=ProductDao.getNewId();
            txtProdCode.setText(str);
            HashMap categories = CategoryDao.getAllCategoryId();
            Collection s=categories.keySet();
            Iterator it=s.iterator();
            while(it.hasNext()){
                jcbCategory.addItem((String)it.next());
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in database", "Error!!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
    
